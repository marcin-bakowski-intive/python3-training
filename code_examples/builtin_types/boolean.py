#!/usr/bin/env python3
# Booleans
# https://docs.python.org/3/library/stdtypes.html#truth-value-testing

a = True
b = False

print("%s AND %s == %s" % (a, b, a and b))
print("%s & %s == %s" % (a, b, a & b))

print("%s OR %s == %s" % (a, b, a or b))
print("%s | %s == %s" % (a, b, a | b))

print("NOT %s == %s" % (a, not a))



